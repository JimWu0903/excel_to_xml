package org.example.business;

public class RecycleInfo {

    private int isBuzEntity;
    private String requestAddress;
    private String cleanerCode;
    private String storeAddress;

    public int getIsBuzEntity() {
        return isBuzEntity;
    }

    public void setIsBuzEntity(int isBuzEntity) {
        this.isBuzEntity = isBuzEntity;
    }

    public String getRequestAddress() {
        return requestAddress;
    }

    public void setRequestAddress(String requestAddress) {
        this.requestAddress = requestAddress;
    }

    public String getCleanerCode() {
        return cleanerCode;
    }

    public void setCleanerCode(String cleanerCode) {
        this.cleanerCode = cleanerCode;
    }

    public String getStoreAddress() {
        return storeAddress;
    }

    public void setStoreAddress(String storeAddress) {
        this.storeAddress = storeAddress;
    }
}
